package com.lxf.repository.po;

public class Customer {
	private String customer_name;
	private String customer_contact;
	private String customer_tel;
	private String customer_address;
	private String customer_other;
	private String customer_id;
	private String customer_addtime;
	private String state;
	private String customer_type;
	public String getCustomer_name() {
		return customer_name;
	}
	public void setCustomer_name(String customer_name) {
		this.customer_name = customer_name;
	}
	public String getCustomer_contact() {
		return customer_contact;
	}
	public void setCustomer_contact(String customer_contact) {
		this.customer_contact = customer_contact;
	}
	public String getCustomer_tel() {
		return customer_tel;
	}
	public void setCustomer_tel(String customer_tel) {
		this.customer_tel = customer_tel;
	}
	public String getCustomer_address() {
		return customer_address;
	}
	public void setCustomer_address(String customer_address) {
		this.customer_address = customer_address;
	}
	public String getCustomer_other() {
		return customer_other;
	}
	public void setCustomer_other(String customer_other) {
		this.customer_other = customer_other;
	}
	public String getCustomer_id() {
		return customer_id;
	}
	public void setCustomer_id(String customer_id) {
		this.customer_id = customer_id;
	}
	public String getCustomer_addtime() {
		return customer_addtime;
	}
	public void setCustomer_addtime(String customer_addtime) {
		this.customer_addtime = customer_addtime;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCustomer_type() {
		return customer_type;
	}
	public void setCustomer_type(String customer_type) {
		this.customer_type = customer_type;
	}
	
}
