package com.lxf.repository.po;

public class SupplierBean {
	private String s_name;
	private String s_contact;
	private String s_tel;
	private String s_address;
	private String s_other;
	public String getS_name() {
		return s_name;
	}
	public void setS_name(String s_name) {
		this.s_name = s_name;
	}
	public String getS_contact() {
		return s_contact;
	}
	public void setS_contact(String s_contact) {
		this.s_contact = s_contact;
	}
	public String getS_tel() {
		return s_tel;
	}
	public void setS_tel(String s_tel) {
		this.s_tel = s_tel;
	}
	public String getS_address() {
		return s_address;
	}
	public void setS_address(String s_address) {
		this.s_address = s_address;
	}
	public String getS_other() {
		return s_other;
	}
	public void setS_other(String s_other) {
		this.s_other = s_other;
	}
	
}
